// main is the entry point for the multichecker tool. It configures and runs various static analysis checks.
func main() {
	// Add standard static analyzers of the golang.org/x/tools/go/analysis/passes package.
	var passesAnalyses []*analysis.Analyzer = []*analysis.Analyzer{
		appends.Analyzer,
		asmdecl.Analyzer,
		assign.Analyzer,
		atomic.Analyzer,
		atomicalign.Analyzer,
		bools.Analyzer,
		buildssa.Analyzer,
		buildtag.Analyzer,
		cgocall.Analyzer,
		composite.Analyzer,
		copylock.Analyzer,
		ctrlflow.Analyzer,
		deepequalerrors.Analyzer,
		defers.Analyzer,
		directive.Analyzer,
		errorsas.Analyzer,
		fieldalignment.Analyzer,
		findcall.Analyzer,
		framepointer.Analyzer,
		httpmux.Analyzer,
		httpresponse.Analyzer,
		ifaceassert.Analyzer,
		inspect.Analyzer,
		loopclosure.Analyzer,
		lostcancel.Analyzer,
		nilfunc.Analyzer,
		nilness.Analyzer,
		pkgfact.Analyzer,
		printf.Analyzer,
		reflectvaluecompare.Analyzer,
		shadow.Analyzer,
		shift.Analyzer,
		sigchanyzer.Analyzer,
		slog.Analyzer,
		sortslice.Analyzer,
		stdmethods.Analyzer,
		stdversion.Analyzer,
		stringintconv.Analyzer,
		structtag.Analyzer,
		testinggoroutine.Analyzer,
		tests.Analyzer,
		timeformat.Analyzer,
		unmarshal.Analyzer,
		unreachable.Analyzer,
		unsafeptr.Analyzer,
		unusedresult.Analyzer,
		unusedwrite.Analyzer,
		usesgenerics.Analyzer}

	var checks []*analysis.Analyzer
	checks = append(checks, passesAnalyses...)

	// Add SA class analyzers from the staticcheck.io package and other specified analyzers.
	staticcheckChecks := map[string]bool{
		"S1018":  true, // Use copy for sliding elements.
		"ST1012": true, // Poorly chosen name for error variable.
		"QF1006": true, // Lift if+break into loop condition.
	}

	for _, v := range staticcheck.Analyzers {
		if strings.HasPrefix(v.Analyzer.Name, "SA") || staticcheckChecks[v.Analyzer.Name] {
			checks = append(checks, v.Analyzer)
		}
	}

	// Add public analyzers for checking unchecked errors and error wrapping.
	checks = append(checks, errcheck.Analyzer) // Check for unchecked errors.
	checks = append(checks, errwrap.Analyzer)  // Wrap errors in fmt.Errorf() calls with the %w verb directive.

	// Add check for os.Exit calls in the main function of the main package.
	checks = append(checks, OsExitChecker)

	multichecker.Main(checks...)
}
